char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		if (i == 0 && (str[i] <= 'z' && str[i] >= 'a'))
			str[i] -= 32;
		else if (i > 0 && !(
				('a' <= str[i - 1] && str[i - 1] <= 'z')
				|| ('A' <= str[i - 1] && str[i - 1] <= 'Z')
				|| ('0' <= str[i - 1] && str[i - 1] <= '9')))
		{
			if (str[i] <= 'z' && str[i] >= 'a')
			{
				str[i] -= 32;
			}
		}
		i++;
	}
	return (str);
}
